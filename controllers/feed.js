const updatePost = (req, res, next) => {
  const errors = validationResult(req);
  if (!errors.isEmpty()) {
    const error = new Error("Validation failed, entered data is incorrect.");
    error.statusCode = 422;
    throw error;
  }

  const postId = req.params.postId;
  const title = req.body.title;
  const content = req.body.content;
  let imageUrl = typeof req.body.image === "string" ? req.body.image : null;
  if (req.file) {
    imageUrl = req.file.path.replace("\\", "/");
  }

  Post.findById(postId)
    .then((post) => {
      if (!post) {
        const error = new Error("Could not find post.");
        error.statusCode = 404;
        throw error;
      } else {
        post.title = title;
        post.content = content;
        if (post.creator.toString() !== req.userId) {
          const error = new Error("Not authorized!");
          error.statusCode = 403;
          throw error;
        }
        if (imageUrl && imageUrl !== post.imageUrl) {
          clearImage(post.imageUrl);
          post.imageUrl = imageUrl;
        }

        return post.save();
      }
    })
    .then((result) => {
      res.status(200).json({ message: "Post updated!", post: result });
    })
    .catch((err) => {
      if (!err.statusCode) {
        err.statusCode = 500;
      }
      next(err);
    });
};

const clearImage = (filePath) => {
  filePath = path.join(__dirname, "..", filePath);
  fs.unlink(filePath, (err) => console.log(err)); //delete the file
};

const deletePost = (req, res, next) => {
  const postId = req.params.postId;
  Post.findById(postId)
    .then((post) => {
      if (!post) {
        const error = new Error("Could not find post.");
        error.statusCode = 404;
        throw error;
      } else {
        if (post.creator.toString() !== req.userId) {
          const error = new Error("Not authorized!");
          error.statusCode = 403;
          throw error;
        }
        clearImage(post.imageUrl);
        return Post.findByIdAndDelete(postId);
      }
    })
    .then((result) => {
      User.findById(req.userId)
        .then((user) => {
          user.posts.pull(postId);
          return user.save();
        })
        .then((result) => {
          res.status(200).json({ message: "Post deleted!" });
        });
    })
    .catch((err) => {
      if (!err.statusCode) {
        err.statusCode = 500;
      }
      next(err);
    });
};

export { getPosts, createPost, getPost, updatePost, deletePost };
